function findPos(obj)
{
	var curleft = 0, curtop = 0;
	if (obj.offsetParent) {
		do {
			curleft += obj.offsetLeft;
			curtop += obj.offsetTop;
		} while (obj = obj.offsetParent);
		return { x: curleft, y: curtop };
	}
	return undefined;
}

function getMouseCoord(obj, e)
{
	var pos, x, y
	pos = findPos(obj);
	x = e.pageX - pos.x;
	y = e.pageY - pos.y;
	return {'x':x, 'y':y};
}

function makeLinks(str, color)
{
	var urlExp, style;
	style = '';
	if( color )
	{
		style = 'style="color:'+ color +'"';
	}
	urlExp = /(\b(www\.|(https?|ftp|file|spring):\/\/)[-A-Z0-9+&@#\/%?=~_|!:,.;]*[-A-Z0-9+&@#\/%=~_|])/ig;
	str = str.replace( urlExp, "<a href='$1' target='_blank' " + style + ">$1</a>" );
	urlExp = /(href='www)/ig;
	str = str.replace( /href='www/gi, "href='http://www" );
	
	return str;
}

